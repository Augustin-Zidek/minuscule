package eu.zidek.augustin.minuscule;

/**
 * Enumeration for positions. Use these to specify where the label should be
 * respective to the given point.
 */
public enum LabelPosition {
	/**
	 * North (above)
	 */
	NORTH,
	/**
	 * Northeast (above to the right)
	 */
	NORTHEAST,
	/**
	 * East (right)
	 */
	EAST,
	/**
	 * Southeast (below to the right)
	 */
	SOUTHEAST,
	/**
	 * South (below)
	 */
	SOUTH;
}
